"""Simple wrapper around QuickJS-NG's qjs-wasi-reactor.wasm."""

import importlib.resources
import json

from wasmjs import lifecycle
from wasmjs import wasmfile

_BOOTSTRAP = """
globalThis.__eval_json = function(src) {
  try {
    return JSON.stringify({ok: true, value: eval(src)});
  } catch (e) {
    return JSON.stringify({ok: false, error: String(e)});
  }
};
"""


class WasmJS:
    """Simple wrapper around QuickJS-NG's qjs-wasi-reactor.wasm."""

    _wasmfile = None

    def __init__(self):
        if not self._wasmfile:
            WasmJS._wasmfile = wasmfile.WasmFile(wasm=importlib.resources.files(
                'qjs-wasi-reactor').joinpath('qjs-wasi-reactor.wasm').read_bytes())

        self._inst = self._wasmfile.instantiate()
        self._inst.exports.qjs_init()
        self._api = _PythonicAPI(self._inst)

        with self._api.eval_to_jsval(_BOOTSTRAP):
            pass

    def eval(self, s):
        """Evaluate s as a JavaScript expression."""

        with self._api.eval_to_jsval(f'__eval_json({json.dumps(s)})') as jsval:
            data = json.loads(jsval.to_string())
        if not data['ok']:
            raise JSError(data['error'])
        return data.get('value')


class JSError(Exception):
    """Any exception generated by the QuickJS runtime."""


class _PythonicAPI:

    def __init__(self, inst):
        self.inst = inst
        self.lowlevel = _CAPI(inst)

    def eval_to_jsval(self, s):
        """Return eval(s) as a JSValue."""

        with self.inst.write_string(s) as written:
            jsval_offset = self.lowlevel.Eval(written.offset, written.size - 1, 0, 0)
        return _JSValue(api=self, offset=jsval_offset)

    def jsval_to_cstr(self, jsval_offset):
        """Return String(JSValue) as a C string."""

        with self.inst.reserve_size_t() as sizet:
            cstr_offset = self.lowlevel.ToCStringLen2(sizet.offset, jsval_offset, 0)
            return _CString(api=self, offset=cstr_offset, utf8_len=sizet.to_int())


class _CString(lifecycle.PythonOwnedObject):

    def to_string(self):
        """Convert this C string to a Python string."""

        return self.api.inst.exports.memory.read(self.offset,
                                                 self.offset + self.utf8_len).decode('utf-8')

    def close(self):
        self.api.lowlevel.FreeCString(self.offset)


class _JSValue(lifecycle.PythonOwnedObject):

    def to_string(self):
        """Convert this JSValue to a Python string."""

        with self.api.jsval_to_cstr(self.offset) as cstr:
            return cstr.to_string()

    def close(self):
        self.api.lowlevel.FreeValue(self.offset)


class _CAPI:  # pylint: disable=invalid-name,missing-function-docstring
    """See https://github.com/quickjs-ng/quickjs/blob/master/quickjs.h."""

    def __init__(self, inst):
        self.inst = inst
        self.ctx = inst.exports.qjs_get_context()

    def Eval(self, input_offset, input_len, filename_offset, eval_flags):
        return self.inst.exports.JS_Eval(self.ctx, input_offset, input_len, filename_offset,
                                         eval_flags)

    def FreeCString(self, cstr_offset):
        return self.inst.exports.JS_FreeCString(self.ctx, cstr_offset)

    def FreeValue(self, jsval_offset):
        return self.inst.exports.JS_FreeValue(self.ctx, jsval_offset)

    def ToCStringLen2(self, sizet_offset, jsvalue_offset, cesu8):
        return self.inst.exports.JS_ToCStringLen2(self.ctx, sizet_offset, jsvalue_offset, cesu8)
