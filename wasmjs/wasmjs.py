"""Simple wrapper around QuickJS-NG's qjs-wasi-reactor.wasm."""

import importlib.resources
import json

from wasmjs import lifecycle
from wasmjs import wasmfile

_BOOTSTRAP = """
globalThis.__eval_json = function(src) {
  try {
    return JSON.stringify({ok: true, value: eval(src)});
  } catch (e) {
    return JSON.stringify({ok: false, error: String(e)});
  }
};
"""


class WasmJS:
    """Simple wrapper around QuickJS-NG's qjs-wasi-reactor.wasm."""

    _wasmfile = None

    def __init__(self):
        if not self._wasmfile:
            WasmJS._wasmfile = wasmfile.WasmFile(wasm=importlib.resources.files(
                'qjs-wasi-reactor').joinpath('qjs-wasi-reactor.wasm').read_bytes())

        self._inst = self._wasmfile.instantiate()
        self._inst.exports.qjs_init()
        self.ctx = _Context(self._inst)

        with self.ctx.Eval(_BOOTSTRAP, 0, 0):
            pass

    def eval(self, s):
        """Evaluate s as a JavaScript expression."""

        with self.ctx.Eval(f'__eval_json({json.dumps(s)})', 0, 0) as jsval:
            data = json.loads(jsval.to_string())
        if not data['ok']:
            raise JSError(data['error'])
        return data.get('value')


class JSError(Exception):
    """Any exception generated by the QuickJS runtime."""


class _Context:  # pylint: disable=invalid-name

    def __init__(self, inst):
        self._inst = inst
        self._ctx = inst.exports.qjs_get_context()

    def Eval(self, s, filename_offset, eval_flags):
        """Evaluate s as a JavaScript expression."""

        with self._inst.write_string(s) as written:
            return _JSValue(ctx=self,
                            offset=self._inst.exports.JS_Eval(self._ctx, written.offset,
                                                              written.size - 1, filename_offset,
                                                              eval_flags))

    def FreeCString(self, cstr_offset):
        """Free [the JSVal holding] the C string in linear memory."""

        return self._inst.exports.JS_FreeCString(self._ctx, cstr_offset)

    def FreeValue(self, jsval_offset):
        """Free the JSVal in linear memory."""

        return self._inst.exports.JS_FreeValue(self._ctx, jsval_offset)

    def ToCStringLen2(self, jsvalue_offset, cesu8):
        """Return String(JSValue) as a C string."""

        with self._inst.reserve_size_t() as sizet:
            cstr_offset = self._inst.exports.JS_ToCStringLen2(self._ctx, sizet.offset,
                                                              jsvalue_offset, cesu8)
            return _CString(ctx=self, offset=cstr_offset, utf8_len=sizet.to_int())


class _CString(lifecycle.PythonOwnedObject):

    def to_string(self):
        """Convert this C string to a Python string."""

        return self.ctx._inst.exports.memory.read(  # pylint: disable=protected-access
            self.offset, self.offset + self.utf8_len).decode('utf-8')

    def close(self):
        self.ctx.FreeCString(self.offset)


class _JSValue(lifecycle.PythonOwnedObject):

    def to_string(self):
        """Convert this JSValue to a Python string."""

        with self.ctx.ToCStringLen2(self.offset, 0) as cstr:
            return cstr.to_string()

    def close(self):
        self.ctx.FreeValue(self.offset)
