"""Simple wrapper around QuickJS-NG's qjs-wasi-reactor.wasm."""

import importlib.resources
import json

from wasmjs import wasmfile


class WasmJS:
    """Simple wrapper around QuickJS-NG's qjs-wasi-reactor.wasm."""

    _wasmfile = None

    def __init__(self):
        if not self._wasmfile:
            WasmJS._wasmfile = wasmfile.WasmFile(wasm=importlib.resources.files(
                'qjs-wasi-reactor').joinpath('qjs-wasi-reactor.wasm').read_bytes())

        self._inst = self._wasmfile.instantiate()
        assert self._inst.exports.memory.pointer_size == 4, (
            'This build of wasmjs assumes JSValues are NaN-boxed int64s.')

        if self._bootstrap:
            with self._inst.api.js.eval_to_jsval(self._bootstrap):
                pass

    _bootstrap = """
        globalThis.__wej = function wasmjs_eval(src) {
          try {
            return JSON.stringify({ok: true, value: eval(src)});
          } catch (e) {
            if (!(e instanceof Error))
              e = new Error(e);
            return JSON.stringify({ok: false, message: e.message, name: e.name, stack: e.stack});
          }
        };
    """

    def eval(self, expr):
        """Evaluate expr as a JavaScript expression."""

        try:
            with self._inst.api.js.eval_to_jsval(f'__wej({json.dumps(expr)})') as jsval:
                data = json.loads(jsval.to_string())
        except wasmfile.wasmtime.Trap as e:
            raise InterpreterError() from e
        if not data['ok']:
            data.pop('ok')
            raise JSError(**data)
        return data.get('value')


class JSError(Exception):
    """Any exception generated by the QuickJS runtime."""

    def __init__(self, *, name, message, stack=None):
        self.name = name
        self.message = message
        self.stack = stack
        super().__init__(f'{name}: {message}')


class InterpreterError(Exception):
    """Any exception generated by the Wasmtime runtime."""
