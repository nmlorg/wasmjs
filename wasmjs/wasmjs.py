"""Simple wrapper around QuickJS-NG's qjs-wasi-reactor.wasm."""

import contextlib
import importlib.resources
import json

from wasmjs import jsvalueutil
from wasmjs import wasmfile


class WasmJS:
    """Simple wrapper around QuickJS-NG's qjs-wasi-reactor.wasm."""

    _wasmfile = None

    def __init__(self):
        if not self._wasmfile:
            WasmJS._wasmfile = wasmfile.WasmFile(wasm=importlib.resources.files(
                'qjs-wasi-reactor').joinpath('qjs-wasi-reactor.wasm').read_bytes())

        self._inst = self._wasmfile.instantiate()
        assert self._inst.exports.memory.pointer_size == 4, (
            'This build of wasmjs assumes JSValues are NaN-boxed int64s.')
        self._api = _PythonicAPI(self._inst)

        if self._bootstrap:
            with self._api.eval_to_jsval(self._bootstrap):
                pass

    _bootstrap = """
        globalThis.__wej = function wasmjs_eval(src) {
          try {
            return JSON.stringify({ok: true, value: eval(src)});
          } catch (e) {
            if (!(e instanceof Error))
              e = new Error(e);
            return JSON.stringify({ok: false, message: e.message, name: e.name, stack: e.stack});
          }
        };
    """

    def eval(self, expr):
        """Evaluate s as a JavaScript expression."""

        try:
            with self._api.eval_to_jsval(f'__wej({json.dumps(expr)})') as jsval:
                data = json.loads(jsval.to_string())
        except wasmfile.wasmtime.Trap as e:
            raise InterpreterError() from e
        if not data['ok']:
            data.pop('ok')
            raise JSError(**data)
        return data.get('value')


class JSError(Exception):
    """Any exception generated by the QuickJS runtime."""

    def __init__(self, *, name, message, stack=None):
        self.name = name
        self.message = message
        self.stack = stack
        super().__init__(f'{name}: {message}')


class InterpreterError(Exception):
    """Any exception generated by the Wasmtime runtime."""


class _PythonicAPI:

    def __init__(self, inst):
        self.inst = inst

    def call_to_jsval(self, func_nanbox, *args, this_nanbox=0):
        """Return func_nanbox.call(this_nanbox, *args) as a JSValue."""

        with contextlib.ExitStack() as stack:
            jsval_nanboxes = []
            for arg in args:
                jsval = stack.enter_context(jsvalueutil.JSValue.encode(self, arg))
                jsval_nanboxes.append(jsval.nanbox.to_bytes(8, 'little', signed=True))
            with self.inst.api.memutil.write_buf(b''.join(jsval_nanboxes)) as written_argv:
                jsval_nanbox = self.inst.api.qjs.JS_Call(func_nanbox, this_nanbox, len(args),
                                                         written_argv.offset)
        return jsvalueutil.JSValue(api=self, nanbox=jsval_nanbox)

    def eval_to_jsval(self, s):
        """Return eval(s) as a JSValue."""

        with self.inst.api.memutil.write_string(s) as written:
            jsval_nanbox = self.inst.api.qjs.JS_Eval(written.offset, written.size - 1, 0, 0)
        return jsvalueutil.JSValue(api=self, nanbox=jsval_nanbox)
